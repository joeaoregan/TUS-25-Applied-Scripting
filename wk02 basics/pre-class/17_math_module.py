import math

x = 10.5

print(math.ceil(x))        # 11
# comb(n, k): number of ways to choose k items from n items without repetition and without order
# copysign(x, y): return a float with the magnitude of x but the sign of y
print(math.exp(x))              # 36315.50267978618
print(math.fabs(x))             # 10.5

print(math.factorial(5))        # 120

print(math.floor(x))       # 10
# math.fsum(seq) # return an accurate floating point sum of values in the iterable seq
# gcd(x, y) # return the greatest common divisor of the integers x and y
# isclose(a, b) # determine whether two floating point numbers are close in value
# isqrt(n) # return the integer square root of the nonnegative integer n
print(math.isqrt(5))      # 2
# lcm(integers) # return the least common multiple of the given integers
# log(x[, base]) # return the logarithm of x to the given base
# log10(x) # return the base-10 logarithm of x
print(math.log10(100))    # 2.0
# modf(x) # return the fractional and integer parts of x
# perm(n, k) # number of ways to choose k items from n items without repetition and with order
# pow(x, y) # return x raised to the power y
print(math.pow(2, 3))     # 8.0
# prod(iterable) # return the product of a start value (default: 1) times an iterable of numbers
# remainder(x, y) # return the IEEE 754-style remainder of x with respect to y
# sqrt(x) # return the square root of x
print(math.sqrt(25))     # 5.0
